  <h1 class="text-white bg-dark">TUJUAN KEHIDUPAN MANUSIA</h1>
    <p  style="text-indent: 45px;">
      1. Untuk merealisasikan diri menjadi semakin sempurna dan utuh. Dengan kata lain kehidupan bagi manusia merupakan kesempatan baginya untuk semakin merealisasikan dirinya. 
      Kesimpulan ini berangkat dari penyelidikan atas fakta bahwa manusia tidak pernah merasa hidupnya sudah sempurna. Sebaliknya ia mengalami dirinya selalu ditarik ke atas ke arah kesempurnaan, 
      kebijaksanaan, keutamaan, kebaikan terus-menerus. Dengan kata lain manusia tidak pernah menerima dirinya sebagai sudah sempurna, sudah utuh sampai kehidupan itu sendiri dialaminya bertemu dengan kematian. 
    <p style="text-indent: 45px;">2. Untuk menjalani proses menuju hakikatnya; Kematian bagi manusia tidak diterima sebagai suatu yang wajar. Ketika seseorang menyadari kematian dirinya, atau orang yang dikasihinya,
      ia buru-buru mengusirnya dari kesadarannya, atau orang tidak mengusir dari kesadarannya tetapi membayangkannya dengan penuh ketakutan. Manusia menolak kematian, padahal kehidupan setiap orang dengan pasti maju secara perlahan menuju ke kematian.
      Hal ini menurut Leahy (1998,107) disebabkan oleh kenyataan bahwa di dalam diri manusia ada benih kehidupan sesudah kematian. Kehancuran tubuh fisik oleh karena kematian, bukannya membuat kehidupan berakhir melainkan tetap berlanjut.
      Dalam hal ini, jiwalah yang berperan. Situasi ini sekaligus menunjukkan tentang jiwa yang rindu akan hakekatnya yakni kehidupan setelah kematian. Dengan demikian tujuan hidup yang kedua ini hendak menegaskan mengenai proses yang harus 
      dilalui manusia dalam perjalanan menuju hakekatnya, yaitu kehidupan setelah kematian. Selanjutnya Leahy (1998;107) tidak ada apapun di dunia ini yang mampu untuk memenuhi hasrat manusia untuk mengalami kebahagiaan yang paripurna dan definitif.
      Setelah seseorang mengalami kebahagiaan, kebahagian itu seolah cepat memudar dan lenyap sehingga menuntut pemenuhan lagi. Hal-hal di dunia ini sering menipu manusia, sebab tampaknya dapat memberikan kebahagiaan yang sempurna, kenyataannya tidak demikian.
      Menurutnya pengalaman manusiawi yang mengajari bahwa tidak ada satu hal di dunia ini yang dapat memberikan kebahagiaan sempurna. Mengusaha kan kebahagiaan sempurna di dunia ini ibarat menyelami sumur tanpa dasar, artinya merupakan usaha yang siasia.
      Berdasarkan hal-hal tersebut dapat disimpulkan bahwa kehidupan yang dirindukan oleh manusia adalah kehidupan bahagia yang paripurna. Kehidupan bahagia yang paripurna tersebut diawali di dunia ini dengan cara mencintai kebijaksanaan/keutamaan/kebaikan sesuai dengan ajaran Plato,
      seorang filsuf yang jiwanya didorong oleh rasa rindu akan yang baik. Salah satu contoh konkret dalam mencintai kebijaksanaan adalah dengan berusaha secara konsisten menjalani kehidupan berdasarkan pada proses kerelaan untuk melepaskan, memberi dan menerima. Menjalani kehidupan
      sebagai proses melepaskan berarti hidup dengan kesadaran bahwa kehidupan adalah proses pelepasan secara terus-menerus sampai pada akhirnya tiba pada suatu pelepasan yang radikal yakni terlepasnya badan dan jiwa. Oleh karena itu sebelum dipaksa oleh kematian untuk melepaskan hal-hal
      yang mengikat, adalah bijaksana untuk melepaskannya secara sadar dengan suka rela. Orang yang sudah dewasa butuh melepaskan masa kecilnya. Jika orang sudah tua, maka ia butuh melepaskan masa mudanya. Setiap orang butuh melepaskan masa lalunya, apapun bentuknya. Setiap orang butuh melepaskan kekuasaannya, dalam berbagai bentuknya. Secara singkat setiap orang butuh melepaskan diri dari kemelekatan terhadap: kenikmatan, kekuasaan, kekayaan dan lain-lain yang menghalanginya untuk menjadi lebih lepas bebas. Menghidupi kehidupan dengan memberi berarti hidup dengan kesadaran untuk rela berbagi dengan yang lain. 
      Setiap orang dapat:</p>
      <p>
      (1) Memberi cinta kasihnya kepada orang lain. <br>
      (2) Memberi perhatiannya. <br>
      (3) Memberi kepada orang lain hak-hak mereka. <br> 
      (4) Memberi maaf dan pengampunan. <br>
      (5) Memberi kegembiraan dan harapan. <br>
      (6) Membei damai.
      </p>
      <p style="text-indent: 45px;"> Memberi merupakan hakikat cinta. Sementara menjalani kehidupan dengan menerima 2. Ia dapat tetapi tidak mau, maka Ia buruk hati, yang hal ini pun tidak terdapat pada Allah. 3. Ia tidak mau dan tidak dapat, maka Ia sekaligus buruk hati dan tak berdaya, artinya Ia bukan Allah, 4. Ia mau dan dapat (mampu) itulah yang tepat untuk Allah maka dari manakah asalnya kejahatan, dan mengapa Ia tidak meniadakannya? Jadi, harus diakui bahwa tidak ada Allah. 
        Leahy (1994:272) mengutip sambutan Roger sebagai berikut; WIDYA 26 Tahun 29 Nomor 319 April 2012 FALSAFAT berarti hidup dengan kesadaran untuk mau memikul tanggung jawab. Setiap orang bertanggung jawab terhadap: </p>
      <p>
      (1) diri dan hidupnya; <br>
      (2) sesamanya,<br>
      (3) semua makhluk hidup,<br> 
      (4) alam sekitarnya:seperti udara, air dan tanah,<br> 
      (5) kelangsungan hidup di planet bumi ini.<br>
      </p>
      <p style="text-indent: 45px;">Berdasarkan uraian sebelumnya tentang kecenderungan jiwa manusia pada kehidupan setelah kematian dan kebahagiaan, dapatlah ditarik kesimpulan bahwa kehidupan setelah kematian benar-benar ada. Seandainya kerinduan semacam itu ada di dalam jiwa manusia tetapi ternyata bahwa kerinduan itu tidak dapat dipenuhi oleh apa pun yang ada di dunia ini maka kecenderungan itu haruslah dipahami sebagai benih yang sengaja ditanam oleh Sang Pencipta dengan maksud tertentu yaitu untuk memperingatkan kita bahwa adanya kecenderungan itu bukan percuma tetapi benarbenar akan dipenuhi setelah kematian tubuh.
      Oleh sebab seandainya Sang Pencipta telah menanamkan benih keabadian itu tetapi tidak memenuhinya maka sama saja dengan mengatakan bahwa Sang Pencipta itu tidak menepati janjinya dan, Jika Sang Pencipta tidak menepati berarti Ia bukan Allah lagi. Mengacu pada fakta yang ada bahwa jiwa manusia berbeda dengan materi. Jiwa berupa realitas spiritual yang dinamis, tanpa keluasan dan kuantitas. Jiwa manusia tidak terdiri atas bagian-bagian seperti materi sehingga jiwa itu tidak bisa bercerai berai atau hancur setelah tubuh mati. Dengan kata lain ciri yang ada pada jiwa membuatnya tidak dapat dikenai oleh hukum-hukum materi seperti pembusukan. 
      Dengan demikian dapat dikatakan bahwa setelah kehancuran badan, jiwa tetap bertahan, yang berarti bahwa adanya kekekalan jiwa menunjuk pada adanya kehidupan setelah kematian</p>
